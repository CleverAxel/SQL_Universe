CREATE DATABASE universe
DEFAULT CHARACTER SET utf8mb4;
USE universe;

CREATE TABLE stars (
	id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(256) NOT NULL,
    radius_km INT
);

CREATE TABLE planet_types (
	id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(256) NOT NULL
);

CREATE TABLE planets (
	id int AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(256) NOT NULL,
    distance_from_star_km BIGINT NOT NULL,
    star_id INT NOT NULL,
    type_id INT NOT NULL,
    radius_km INT NOT NULL,
    CONSTRAINT fk_planets_star FOREIGN KEY(star_id) REFERENCES stars(id),
    CONSTRAINT fk_planets_planet_type FOREIGN KEY(type_id) REFERENCES planet_types(id)
);

CREATE TABLE moons(
	id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(256) NOT NULL,
    planet_id INT NOT NULL,
    radius_km INT NOT NULL,
    CONSTRAINT fk_moons_planet FOREIGN KEY(planet_id) REFERENCES planets(id)
);


